function colocarImg1(srcImagem){
let imagemPrincipal = document.querySelector('.imagem-fruta-principal')
let corDeFundo = document.querySelector('body')

imagemPrincipal.setAttribute("src",srcImagem)
corDeFundo.setAttribute("class","vermelho")

}

